
package com.singlefood.sinfo.models.productos;


public class Imagenbitmap {

    private Integer allocationByteCount;
    private Integer byteCount;
    private String config;
    private Integer density;
    private Integer generationId;
    private Integer height;
    private Boolean mutable;
    private Boolean premultiplied;
    private Boolean recycled;
    private Integer rowBytes;
    private Integer width;

    public Integer getAllocationByteCount() {
        return allocationByteCount;
    }

    public void setAllocationByteCount(Integer allocationByteCount) {
        this.allocationByteCount = allocationByteCount;
    }

    public Integer getByteCount() {
        return byteCount;
    }

    public void setByteCount(Integer byteCount) {
        this.byteCount = byteCount;
    }

    public String getConfig() {
        return config;
    }

    public void setConfig(String config) {
        this.config = config;
    }

    public Integer getDensity() {
        return density;
    }

    public void setDensity(Integer density) {
        this.density = density;
    }

    public Integer getGenerationId() {
        return generationId;
    }

    public void setGenerationId(Integer generationId) {
        this.generationId = generationId;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Boolean getMutable() {
        return mutable;
    }

    public void setMutable(Boolean mutable) {
        this.mutable = mutable;
    }

    public Boolean getPremultiplied() {
        return premultiplied;
    }

    public void setPremultiplied(Boolean premultiplied) {
        this.premultiplied = premultiplied;
    }

    public Boolean getRecycled() {
        return recycled;
    }

    public void setRecycled(Boolean recycled) {
        this.recycled = recycled;
    }

    public Integer getRowBytes() {
        return rowBytes;
    }

    public void setRowBytes(Integer rowBytes) {
        this.rowBytes = rowBytes;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

}
